// RevenueCat key — supply via local.properties (`revenuecat.api.key=...`) or
// the REVENUECAT_API_KEY env var. Falls back to empty (purchases disabled).
val revenueCatApiKey: String = run {
    val props = Properties()
    val f = rootProject.file("local.properties")
    if (f.exists()) f.inputStream().use { props.load(it) }
    props.getProperty("revenuecat.api.key")
        ?: System.getenv("REVENUECAT_API_KEY")
        ?: ""
}

